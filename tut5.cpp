#include <iostream>
using namespace std;
int main(){
    int num1, num2;
    cout<<"Enter the value of num1:";cin>>num1;
    cout<<"\nEnter the value of num2:";cin>>num2;
    cout<<"\nSum of num1 and num2 = "<<num1 + num2;
    return 0;
}
// "<<" is called Insertion operator
// ">>" is called Extraction operator
